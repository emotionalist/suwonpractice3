package com.cookandroid.suwonpractice3;

public class imageList {
    int Listimg,Num;


    public imageList(int Listimg, int Num){
        this.Listimg=Listimg;
        this.Num=Num;
    }

    public int getNum() {
        return Num;
    }

    public void setNum(int num) {
        Num = num;
    }


    public int getListimg() {
        return Listimg;
    }

    public void setListimg(int listimg) {
        Listimg = listimg;
    }
}

